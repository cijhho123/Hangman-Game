#כתבו תוכנית שקולטת מהמשתמש מחרוזת ומדפיסה 'OK' אם היא פלינדרום, אחרת 'NOT'.

#הנחיות
#פלינדרום הוא מחרוזת (מילה, מספר, או כל רצף תווים) שקריאתו מימין לשמאל זהה לקריאתו משמאל לימין. שימו לב:
#אין חשיבות לאותיות קטנות או גדולות.
#התעלמו מרווחים.
#אין להשתמש בלולאות.


print("Please enter a phrase:")
word = input().lower().replace(" ", "")
reverse = word[-1::-1]

if(word == reverse):
    print("OK")
else:
    print("NOT")